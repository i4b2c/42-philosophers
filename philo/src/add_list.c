#include "../include/philosopher.h"

void	adicionar_na_lista(t_geral **g, t_geral *t, t_geral **u)
{
	if (*g == NULL)
	{
		*g = t;
		*u = *g;
	}
	else
	{
		(*u)->next = t;
		*u = t;
	}
}
